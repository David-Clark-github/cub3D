#include "include.h"

int	empty_line_map(char **map)
{
	int	x;
	int	y;

	x = 0;
	y = 0;
	while (map[y])
	{
		if (ft_strlen(map[y]) == 0)
			return (-1);		
		y++;
	}
	return (1);
}
